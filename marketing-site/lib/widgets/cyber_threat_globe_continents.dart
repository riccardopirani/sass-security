bool _isLandCell(double lat, double lon) {
  for (final b in _kLandBoxes) {
    if (lat >= b.latMin &&
        lat <= b.latMax &&
        lon >= b.lonMin &&
        lon <= b.lonMax) {
      return true;
    }
  }
  return false;
}

